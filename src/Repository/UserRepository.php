{

    private $paginator;

    public function __construct(ManagerRegistry $registry, PaginatorInterface $paginator)
    {
        parent::__construct($registry, User::class);
        $this->paginator = $paginator;
    }

    /**
     * @param string|null $term
     * @return Users[]
     */
    public function findAllUsers(?string $que): QueryBuilder
    {
        $qc = $this->createQueryBuilder('v');

        if ($que) {
            $qc->andWhere('v.usenrame LIKE :que OR v.roles LIKE :que')
                ->setParameter('que', '%' . $que . '%');
        }

        return $qc
            ->orderBy('v.id', 'DESC');
        ;
    }
