    /**
     * @param string|null $term
     * @return Genre[]
     */
    public function findAllAuthors(?string $aut): QueryBuilder
    {
        $qa = $this->createQueryBuilder('h');

        if ($aut) {
            $qa->andWhere('h.fullName LIKE :aut')
                ->setParameter('aut', '%' . $aut . '%');
        }

        return $qa
            ->orderBy('h.id', 'ASC');
        ;
    }

